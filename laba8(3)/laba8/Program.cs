// Абстрактна фабрика для створення компонентів технологічних продуктів
public abstract class АбстрактнаФабрика
{
    public abstract Екран СтворитиЕкран();
    public abstract Процесор СтворитиПроцесор();
    public abstract Камера СтворитиКамеру();
}

// Конкретна фабрика для створення компонентів смартфона
public class ФабрикаСмартфона : АбстрактнаФабрика
{
    public override Екран СтворитиЕкран()
    {
        return new ЕкранСмартфона();
    }

    public override Процесор СтворитиПроцесор()
    {
        return new ПроцесорСмартфона();
    }

    public override Камера СтворитиКамеру()
    {
        return new КамераСмартфона();
    }
}

// Конкретна фабрика для створення компонентів ноутбука
public class ФабрикаНоутбука : АбстрактнаФабрика
