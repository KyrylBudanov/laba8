using System;

// Абстрактний клас або інтерфейс для графіків
public abstract class Графік
{
    public abstract void Намалювати();
}

// Конкретний клас для лінійного графіку
public class ЛінійнийГрафік : Графік
{
    public override void Намалювати()
    {
        Console.WriteLine("Малюю Лінійний Графік");
        // Логіка для малювання лінійного графіку
    }
}

// Конкретний клас для стовпчикового графіку
public class СтовпчиковийГрафік : Графік
{
    public override void Намалювати()
    {
        Console.WriteLine("Малюю Стовпчиковий Графік");
        // Логіка для малювання стовпчикового графіку
    }
}

// Конкретний клас для кругової діаграми
public class КруговаДіаграма : Графік
{
    public override void Намалювати()
    {
        Console.WriteLine("Малюю Кругову Діаграму");
        // Логіка для малювання кругової діаграми
    }
}

// Абстрактний клас або інтерфейс для фабрики графіків
public abstract class ФабрикаГрафіків
{
    public abstract Графік СтворитиГрафік();
}

// Конкретна фабрика для лінійного графіку
public class ЛінійнийГрафікФабрика : ФабрикаГрафіків
{
    public override Графік СтворитиГрафік()
    {
        return new ЛінійнийГрафік();
    }
}

// Конкретна фабрика для стовпчикового графіку
public class СтовпчиковийГрафікФабрика : ФабрикаГрафіків
{
    public override Графік СтворитиГрафік()
    {
        return new СтовпчиковийГрафік();
    }
}

// Конкретна фабрика для кругової діаграми
public class КруговаДіаграмаФабрика : ФабрикаГрафіків
{
    public override Графік СтворитиГрафік()
    {
        return new КруговаДіаграма();
    }
}

class Програма
{
    static void Main()
    {
        Console.WriteLine("Введіть тип графіка (Line/Bar/Pie):");
        string типГрафіка = Console.ReadLine();

        // Використання Factory Method для створення відповідного графіка
        ФабрикаГрафіків фабрика;
        switch (типГрафіка.ToLower())
        {
            case "line":
                фабрика = new ЛінійнийГрафікФабрика();
                break;
            case "bar":
                фабрика = new СтовпчиковийГрафікФабрика();
                break;
            case "pie":
                фабрика = new КруговаДіаграмаФабрика();
                break;
            default:
                Console.WriteLine("Невірний тип графіка");
                return;
        }

        // Створення графіка
        Графік графік = фабрика.СтворитиГрафік();

        // Малювання графіка
        графік.Намалювати();
    }
}
